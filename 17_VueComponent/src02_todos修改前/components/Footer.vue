<template>
   <div class="todo-footer">
      <label>
      <input type="checkbox" v-model="isCheckAll" />                                                                                   
      </label>
      <span>
      <span>已完成{{completeSize}}</span> / 全部{{todos.length}}
      </span>
      <button class="btn btn-danger" v-show="completeSize>0"  @click="clearCompleteTodos">
		  清除已完成任务</button>
   </div>
</template>

<script type ="text/ecmascript-6">
	/* 
		模板数据的来源                       
			1.data：自身的可变数据
			2.props：从外部接收的可变数据
			3.computed:根据已有的data或props数据计算产生的数据
	*/


   export default {
	   props:{
		   todos:Array,
		   checkAllTodos:Function,
		   clearCompleteTodos:Function
	   },

	   computed:{
		   completeSize(){	//统计被选中任务的数量
			   return this.todos.reduce((pre,todo) => pre + (todo.complete? 1 : 0),0)                                

				/*reduce():根据数组中元素进行统计处理 
			   const arr = [1,3,4,5,7]
			   const total = arr.reduce((preTotal, item) => preTotal + (item%2===1? item : 0), 0)

			   let result = 0
			   arr,forEach(item => {
				   result += item
			   }) */
		   },


		   isCheckAll: {	
			   get() {
				//通过判断completeSize的值是否等于todos.length来提供布尔值控制v-modele
				//思路：列表控制汇总是否勾选:统计选中的任务数量和全部的任务数量是否相等来提供boolean
				//不能直接completeSize()
			   		return this.todos.length === this.completeSize  && this.completeSize>0
			   },

				set(value) {	//操作checkbox		value代表当前勾选的状态
					//对todos进行全选/全部选操作
					//思路：汇总控制所有列表是否勾选
					this.checkAllTodos(value) 

			   }
		   }
	   }

		
   }
</script>

