<template>
  <div class="todo-header">
    <input type="text" placeholder="请输入你的任务名称，按回车键确认" 
	@keyup.enter="add" 	v-model="title"	/>
  </div>
</template>

<script type ="text/ecmascript-6">
	export default {
		props:{
			addTodo:{	//限定了：属性的名字/属性值的的类型/属性的必要性
				type:Function,
				required:true
			}

		},

		data(){
			return{
				title:''

			} 
		},

		methods:{
				add(){
				//1.根据输入创建一个todo
				const title = this.title.trim()	//如果要自动过滤用户输入的首尾空白字符可以给 v-model 添加 trim 修饰符：
				if(!title){
					return
				}
				const todo = {
					id:Date.now(),
					title,
					complete : false,
				}
				//2.添加todo
				this.addTodo(todo)
				//3.清除输入
				this.title = ''

			}
		}




	};
</script>

